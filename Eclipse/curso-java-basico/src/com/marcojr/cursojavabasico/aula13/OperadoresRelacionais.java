package com.marcojr.cursojavabasico.aula13;

public class OperadoresRelacionais {

	public static void main(String[] args) {
		
		int valor1 = 10;
		int valor2 = 30;
		
		System.out.println("valor1 == valor2? "+ (valor1 == valor2));
		System.out.println("valor1 != valor2? "+ (valor1 != valor2));
		System.out.println("valor1 > valor2? "+ (valor1 > valor2));
		System.out.println("valor1 < valor2? "+ (valor1 < valor2));
		System.out.println("valor1 >= valor2? "+ (valor1 >= valor2));
		System.out.println("valor1 <= valor2? "+ (valor1 <= valor2));
		

	
	}

}
